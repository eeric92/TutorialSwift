import UIKit

// Strings
let myFirstString: String = "Hello Swift"
let mySecondString = "World!"
let myThirdString = myFirstString + " " + mySecondString

print("String datatype -> " , myThirdString)

// Int
let myFirstInt: Int = 1
let mySecondInt = 2
let myThirdInt = myFirstInt + mySecondInt

print("Int datatype -> " , myThirdInt)

// Double -> Utilizar cuando el dato es mas grande que con Float
let myFirstDouble: Double = 1.444
let mySecondDouble = 1.323
let myThirdDouble = myFirstDouble + mySecondDouble

print("Double datatype -> " ,myThirdDouble)

// Float
let myFirstFloat: Float = 1.5
let mySecondFloat: Float = 2.1
let myThirdFloat = myFirstFloat + mySecondFloat

print("Float datatype -> " , myThirdFloat)

// Bool
let myFirstBool = true
let mySecondBool = false
print (myFirstBool && mySecondBool)
print (myFirstBool == mySecondBool)

